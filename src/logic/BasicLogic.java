package logic;

public class BasicLogic {
	
	public Double add(Number a, Number b) {return a.doubleValue() + b.doubleValue();}
	public Double subtract(Number a, Number b) {return a.doubleValue() - b.doubleValue();}
	public Double multiply(Number a, Number b) {return a.doubleValue() * b.doubleValue();}
	public Double divide(Number a, Number b) {return a.doubleValue() / b.doubleValue();}
	
}